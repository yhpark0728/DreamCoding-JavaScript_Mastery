// if(조건) { } else {}
// if(조건1) { } else if(조건2) {} else {}
let fruit = 'orange';
if (fruit === 'apple') {
  console.log('apple');
} else if (fruit === 'orange') {
  console.log('orange');
} else {
  console.log('grape');
}

